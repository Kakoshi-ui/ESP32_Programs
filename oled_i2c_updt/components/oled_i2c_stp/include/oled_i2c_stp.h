#define SET_COM_PINS                    0xDA

//* Timing & Driving Scheme setting command
#define SET_DISPLAY_CLOCK               0xD5
#define SET_PRE_CHARGE_PERIOD           0xD9
#define SET_VCOMH_DESELECT_LEVEL        0xDB
#define NOP                             0xE3

